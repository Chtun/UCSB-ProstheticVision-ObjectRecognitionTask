public struct SimulationVariables
{
    public float r1; 
    public float r2;
    public float r3;
    public float ca;
    public float r4a;
    public float r4b;
    public float r4c;
    public float t;

    public SimulationVariables(float _r1, float _r2, float _r3, float _ca, float _r4a, float _r4b, float _r4c,
        float _t)
    {
        r1 = _r1;
        r2 = _r2;
        r3 = _r3;
        ca = _ca;
        r4a = _r4a;
        r4b = _r4b;
        r4c = _r4c;
        t = _t;
    }
}